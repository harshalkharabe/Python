"""Relational Operators or Comparison Operators
Relational operators are used to compare values.
Relational Operators are binary operators, these operators required 2
operands.

Operator    Description
>           Greater than
<           Less than
>=          Greater than or equal
<=          Less than or equal
!=          Not Equal
==          Equal

Relational operators after comparing values, it returns Boolean value
(True/False). An expression having relational operators is called Boolean
expression."""

if 4>3:
    print(True)
if 4>=3:
    print(True)

if 6<9:
    print(True)
if 6<=9:
    print(True)

if 3==3:
    print(True)

if 2 != 3:
    print(True)